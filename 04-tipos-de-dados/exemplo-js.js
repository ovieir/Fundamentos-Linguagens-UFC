// Exemplo em JavaScript – Tipagem Dinâmica e Fraca
let x = 10;
let y = "5";

console.log(x + y); // Resultado: "105" (concatenação!)
console.log(x - y); // Resultado: 5 (conversão implícita de "5" para número)

//JavaScript realiza coerções automáticas. Exemplo 1 trata valores como string; exemplo 2 converte string para número.
